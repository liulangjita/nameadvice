def CheckBazi(bazi):
    baziLen = len(bazi)
    if baziLen != 6 and baziLen != 8:
        return False
    i = 0
    while i < baziLen:
        ch = bazi[i]
        for j in range(0,10):
            if ch == TianGan[j]:
                break
        if j > 10 or j==10:
            return False
        i = i+1

        ch = bazi[i]
        for j in range(0,12):
            if ch==DiZhi[j]:
                break
        if j > 12 or j==12:
            return False
        i = i+1
    return True


